package com.example.springbootmodelview;

public class ModelObjectClass {
    
    private int moId;
    private String moName;
    private String moText;

	public int getMoId() {
		return moId;
	}
	public void setMoId(int moId) {
		this.moId = moId;
	}
	public String getMoName() {
		return moName;
	}
	public void setMoName(String moName) {
		this.moName = moName;
	}
	public String getMoText() {
		return moText;
	}
	public void setMoText(String moText) {
		this.moText = moText;
	}

}
